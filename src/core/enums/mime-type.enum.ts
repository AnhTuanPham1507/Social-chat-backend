    // Image MIME Types
    JPEG = 'image/jpeg',
    PNG = 'image/png',
    GIF = 'image/gif',
    WEBP = 'image/webp',
    SVG = 'image/svg+xml',
    TIFF = 'image/tiff',
    BMP = 'image/bmp',

    // Video MIME Types
    MP4 = 'video/mp4',
    WEBM = 'video/webm',
    OGG_VIDEO = 'video/ogg',
    AVI = 'video/avi',
    MPEG = 'video/mpeg',
    QUICKTIME = 'video/quicktime',

    // Audio MIME Types
    MP3 = 'audio/mpeg',
    WAV = 'audio/wav',
    OGG_AUDIO = 'audio/ogg',
    WEBM_AUDIO = 'audio/webm',
    FLAC = 'audio/flac',
    AAC = 'audio/aac',

    // Document MIME Types
    PDF = 'application/pdf',
    MS_WORD = 'application/msword',
    MS_POWERPOINT = 'application/vnd.ms-powerpoint',
    RTF = 'application/rtf',
    ZIP = 'application/zip',
